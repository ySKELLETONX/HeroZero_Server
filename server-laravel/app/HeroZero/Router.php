    public static function dispatch(string $action, array $params)
    {
        if (isset(self::HANDLERS[$action])) {
            $handler = require __DIR__ . '/../../actions/' . self::HANDLERS[$action];
            return $handler($params);
        }
        if (Replay::has($action)) {
            return Replay::data($action);
        }
        $userId = (int)($params['user_id'] ?? $params['existing_user_id'] ?? 0);
        if ($userId > 0 && (getenv('HZ_ENV') ?: 'development') !== 'production') {
            error_log("[herozero] action sem handler, devolvendo accountState: {$action}");
            return Live::accountState($userId);
        }
        throw new GameError('errActionNotSupported');
    }
